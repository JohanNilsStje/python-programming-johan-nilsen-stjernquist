
#Circle Class
class Circle:
  def __init__(self, x, y, radius):
    self.x = x
    self.y = y
    self.radius = radius



#Rectangle Class
class Rectangle:
  def __init__(self, x, y, side1, side2):
    self.x_pos = x
    self.y_pos = y
    self.side1 = side1
    self.side2 = side2

